package Com.te.logicprogram;

public class SwapTwono {

	public static void main(String[] args) {
		float f1=8.9f,f2=7.6f;
		System.out.println("Before");
		System.out.println(f1);
		System.out.println(f2);
		float temp=f1;
		f1=f2;
		f2=temp;
		System.out.println("After");
		System.out.println(f1);
		System.out.println(f2);

	}

}
